# Realiza una función llamada area_rectangulo (base, altura) que devuelva el
# área del rectangulo a partir de una base y una altura.
# Calcula el área de un rectángulo de 15 de base y 10 de altura:
# Recordatorio: El área de un rectángulo se obtiene al multiplicar la base por la altura.

def area_rectangulo(base, altura):
    area = base * altura
    return area

print(area_rectangulo(15, 10))