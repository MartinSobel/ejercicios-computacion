# Realiza una función llamada intermedio(a, b) que a partir de dos números,
# devuelva su punto intermedio. Cuando lo tengas comprueba el punto intermedio entre -12 y 24:
# Recordatorio: El número intermedio de dos números corresponde a la suma de los dos números dividida entre 2

def intermedio(a,b):
    """Calcula el punto intermedio entre a y b"""
    punto=(a+b)/2
    return punto

print(intermedio(-12,24))