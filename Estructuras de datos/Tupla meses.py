# Crea una tupla con los meses del año. Pedir el número de mes y mostrar su nombre usando la tupla. (Ver resolución próxima página)

meses = ('Enero', 'Febrero', 'Marzo', 'Abril', 'Mayo', 'Junio', 'Julio', 'Agosto', 'Septiembre', 'Octubre', 'Noviembre', 'Diciembre')
